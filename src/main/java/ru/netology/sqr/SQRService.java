package ru.netology.sqr;

public class SQRService {
    /**
     * Перебирает числа от 10 до 99 и считает, сколько их квадратов
     * попало в заданный диапазон (включая границы).
     *
     * @param lowerBound Нижняя граница диапазона (включительно)
     * @param upperBound Верхняя граница диапазона (включительно)
     * @return Количество чисел, квадрат которых попал в диапазон
     */
    public int calculate(int lowerBound, int upperBound) {
        int count = 0; // Переменная-счетчик для результата

        // Цикл перебора чисел от 10 до 99 (включительно)
        for (int i = 10; i <= 99; i++) {
            int sqr = i * i; // Вычисляем квадрат текущего числа

            // Проверяем, попадает ли квадрат в заданный диапазон
            if (sqr >= lowerBound && sqr <= upperBound) {
                count++; // Если попал, увеличиваем счетчик
            }
        }
        return count;
    }
}